package beans;



public class JobTitle{

	private int jobTitleId;
	
	private String name;

	public int getJobTitleId() {
		return jobTitleId;
	}

	public String getName() {
		return name;
	}

	public void setJobTitleId(int jobTitleId) {
		this.jobTitleId = jobTitleId;
	}

	public void setName(String name) {
		this.name = name;
	}



}
