package beans;

public class District {

	private int districtId;

	private String name;

	public int getDistrictId() {
		return districtId;
	}

	public String getName() {
		return name;
	}

	public void setDistrictId(int districtId) {
		this.districtId = districtId;
	}

	public void setName(String name) {
		this.name = name;
	}

}
